   mxSetField( pMXHdr, 0, "iStartPosV", createInt32Scalar( pHdr->iStartPosV ) );
   
   mxSetField( pMXHdr, 0, "dwTrialFlags", createUInt32Scalar( pHdr->dwTrialFlags ) );
   mxSetField( pMXHdr, 0, "iSTSelected", createInt32Scalar( pHdr->iSTSelected ) );

