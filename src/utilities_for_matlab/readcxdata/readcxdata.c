   mxArray* pMxArr = mxCreateNumericMatrix( 1, 1, mxUINT32_CLASS, mxREAL );
   if( pMxArr != NULL ) *((DWORD*) mxGetData( pMxArr )) = dwValue;
   return( pMxArr );
